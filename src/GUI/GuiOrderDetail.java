    private void txtSoluongKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSoluongKeyReleased
        // TODO add your handling code here:
        //hàm bat loi nhap 
        listproduct = prodll.getAll();
        int rowPro = cbSanpham.getSelectedIndex();
        int idPro = listproduct.get(rowPro).getquantity();
        Pattern pattern = Pattern.compile("\\d*");
        Matcher matcher = pattern.matcher(txtSoluong.getText());
        if (!matcher.matches()) {
            JOptionPane.showMessageDialog(this, "Số lượng là số", "Warning", JOptionPane.WARNING_MESSAGE);
            txtSoluong.requestFocus();
            txtSoluong.setText("");
        } else if (Integer.parseInt(txtSoluong.getText()) - idPro > 0) {
            JOptionPane.showMessageDialog(this, "Số lượng trong kho không đủ");
            txtSoluong.setText(String.valueOf(idPro));

        } else {

            //  int row=tbshow.getSelectedRow();
            int idPro1 = listproduct.get(rowPro).getproid();
            String kt = txtSoluong.getText();

            if (!kt.isEmpty()) {
                int giapro = listproduct.get(idPro1).getprice();
                int Soluong = Integer.parseInt((txtSoluong.getText()));

                int gia = Soluong * giapro;
                txtGia.setText(String.valueOf(gia));
                //  String giaf1=txtGia.getText();
            }
        }
    }//GEN-LAST:event_txtSoluongKeyReleased


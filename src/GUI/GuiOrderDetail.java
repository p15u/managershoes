        orde.setPayment_method(idPay);
        orde.setOrderID(Orderid);
        if (ordedll.AddData(orde)) {
            JOptionPane.showMessageDialog(this, "Them thanh cong");
            Bliding();
            listproduct = prodll.getAll();

            Product pro1 = new Product();
            int rowsp = cbSanpham.getSelectedIndex();
            int idsp = listproduct.get(rowsp).getproid();
            int SPpass = listproduct.get(rowPro).getquantity();
            int sl = Integer.parseInt(txtSoluong.getText());
            sl = SPpass - sl;
            pro1.setquantity(sl);
            pro1.setproId(idsp);
            if (prodll.UpdateSL(pro1)) {
                JOptionPane.showMessageDialog(this, "Đã cập nhập số lượng");
                
            }
            txtDate.setText("");
            txtGia.setText("");
            txtMaDetailHD.setText("");
            txtSoluong.setText("");
            txtmadon.setText("");

        } else {
            JOptionPane.showMessageDialog(this, "Them that bai");
        }


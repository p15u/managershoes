    // phương thực bảng động và add các giá trị vào bảng
    public void Bliding() {
        listorderdetail = ordedll.getAll();
        // số cột được add
        Vector colums = new Vector();
        colums.add("Mã chi tiết hóa đơn");
        colums.add("Ngày Lập");
        colums.add("Tên khách hàng");
        colums.add("Sản phẩm");
        colums.add("Số lượng");
        colums.add("Đơn giá");
        colums.add("Phương thức thức thanh toán");
        colums.add("Mã đơn");
        // số hàng đưuọc add
        Vector data = new Vector();
        // vọng lặp in gia trị
        for (OrderDetail orde : listorderdetail) {
            Vector row = new Vector();
            row.add(orde.getOrderDetailId());
            row.add(orde.getDate());
            listuser = userdll.getbyID(orde.getUserID());
            if (listuser.size() > 0) {
                row.add(listuser.get(0).getFullname());
            } else {
                row.add("Chua nhap");
            }
            listproduct = prodll.getbyIDfromOrder(orde.getProductID());
            if (listproduct.size() > 0) {
                row.add(listproduct.get(0).getname());
            } else {
                row.add("Chua nhap");
            }
            row.add(orde.getQuantity());
            row.add(orde.getPrice());
            row.add(orde.getPayment_method());
            row.add(orde.getOrderID());
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data, colums);
        // đưa vào bảng
        tbshow.setModel(dtm);
    }


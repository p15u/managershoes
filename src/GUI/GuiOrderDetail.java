    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        //button xoa thong tin san pham
        if (tbshow.getSelectedRow() != -1) {
            int row = tbshow.getSelectedRow();
            listorderdetail = ordedll.getAll();
            listproduct = prodll.getAll();
            int sl = Integer.parseInt(txtSoluong.getText());
            int getidrowPro = cbSanpham.getSelectedIndex();
            int idPro12 = listproduct.get(getidrowPro).getproid();
            int SL = listproduct.get(getidrowPro).getquantity();

            if (ordedll.DelData(listorderdetail.get(row).getOrderDetailId())) {
                JOptionPane.showMessageDialog(this, "Xoa thanh cong");
                Bliding();

                int kq = JOptionPane.showConfirmDialog(this, "Bạn có muốn cập nhập lại số lượng sản phẩm ko?", "Thông báo", JOptionPane.YES_OPTION);
                if (kq == JOptionPane.YES_OPTION) {
                    SL = SL + sl;
                    Product pro1 = new Product();
                    pro1.setquantity(SL);
                    pro1.setproId(idPro12);
                    if (prodll.UpdateSL(pro1)) {
                        JOptionPane.showMessageDialog(this, "Đã cập nhập số lượng");
                    }

                }
            } else {
                JOptionPane.showMessageDialog(this, "Update that bai");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Chon dong can xoa");
        }
    }//GEN-LAST:event_btnxoaActionPerformed


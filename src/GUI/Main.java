    public void Bliding() {
        listpro = prodll.getAll();
        Vector culms = new Vector();
        culms.add("Mã sản phâm");
        culms.add("Tên sản phẩm");
        culms.add("Nhãn hiệu");
        culms.add("Giá");
        culms.add("Số lượng");
        culms.add("Kích cỡ");
        culms.add("Category");
        // số hàng đưuọc add
        Vector data = new Vector();
         // vọng lặp in gia trị
        for (Product pro : listpro) {
            Vector row = new Vector();
            row.add(pro.getproid());
            row.add(pro.getname());
            row.add(pro.getbrand());
            row.add(pro.getprice());
            row.add(pro.getquantity());

            listpro = prodll.getbyID(pro.getproid());
            try {
                if (listpro.size() > 0) {
                    row.add(listpro.get(0).getDesc());
                } else {
                    row.add("Không có");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

            listcate = catedll.getByID(pro.getcatid());
            try {
                if (listcate.size() > 0) {
                    row.add(listcate.get(0).getname());
                } else {
                    row.add("Không có");
                }
            } catch (Exception e) {
                e.printStackTrace();
                System.out.println("Ko có dư liệu!!!!!");
            }

            data.add(row);

            //  row.add(pro.getDesc());
            // row.add(pro.getcatid());
        }
        DefaultTableModel dtm = new DefaultTableModel(data, culms);
        tbproduct.setModel(dtm);
    }

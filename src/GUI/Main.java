    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        //them thong tin sảm phảm mới vào sql
        listcate = catedll.getAll();
        listpro = prodll.getAll();
        int gia = 0;
        int soluong = 0;
        int Proid = 0;
        int Cate = cbCate.getSelectedIndex();
        String Tensp = txttensp.getText();
        String brand = txtbrand.getText();
        String Des = String.valueOf(cbSize.getSelectedItem());
        int Cateid = listcate.get(Cate).getcatid();
        if (Tensp.isEmpty() || brand.isEmpty() || Des.isEmpty() || txtgia.getText().isEmpty() || txtsoluong.getText().isEmpty()
                || txtmasp.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Khong duoc de trong Thong tin sp !!");
        } else {
            Product pr = new Product();
            Proid = Integer.parseInt(txtmasp.getText());
            soluong = Integer.parseInt(txtsoluong.getText());
            gia = Integer.parseInt(txtgia.getText());
            pr.setproId(Proid);
            pr.setquantity(soluong);
            pr.setprice(gia);
            pr.setname(Tensp);
            pr.setbrand(brand);
            pr.setDesc(Des);
            pr.setcatid(Cateid);
            if (prodll.Adddata(pr)) {
                Bliding();
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                txtbrand.setText("");
                txtfind.setText("");
                txtmasp.setText(String.valueOf(Proid + 1));
                txtgia.setText("");
                txtsoluong.setText("");
                txttensp.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
            }
        }
    }//GEN-LAST:event_btnthemActionPerformed


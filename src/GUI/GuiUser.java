    public void Bliding() {
        listuser = userdll.getAll();
        Vector clums = new Vector();
        clums.add("Mã khach hàng");
        clums.add("Ten khách hàng");
        clums.add("Số điện thoại");
        clums.add("Địa chỉ");
        clums.add("Tuổi");
         // số hàng đưuọc add
        Vector data = new Vector();
        for (User us : listuser) {
            Vector row = new Vector();
            row.add(us.getUserid());
            row.add(us.getFullname());
            row.add(us.getphone());
            row.add(us.getAddress());
            row.add(us.getage());
            data.add(row);

        }
        DefaultTableModel dtm = new DefaultTableModel(data, clums);
        // đưa vào bảng
        tbshow.setModel(dtm);

    }


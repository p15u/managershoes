    private void txtIDKHKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIDKHKeyReleased
        // TODO add your handling code here:
        //hàm bat loi nhap  
        Pattern pattern = Pattern.compile("\\d*");
        Matcher matcher = pattern.matcher(txtIDKH.getText());
        if (!matcher.matches()) {
            JOptionPane.showMessageDialog(this, "Mã là số", "Warning", JOptionPane.WARNING_MESSAGE);
            txtIDKH.requestFocus();
            txtIDKH.setText("");
        }

        int row = tbshow.getRowCount();
        int ma = row + 1;

       
        for (int i = 0; i < row; i++) {
            if (txtIDKH.getText().equals(String.valueOf(tbshow.getValueAt(i, 0)))) {
                JOptionPane.showMessageDialog(this, "Không được trùng mã");
             
                txtIDKH.setText("");
            }
        }

    }//GEN-LAST:event_txtIDKHKeyReleased

